package Beans;

public class Actors{

	private int Id;
	private String Name;
	private int Age;
	private char Sex;
	private int Rating;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}
	public char getSex() {
		return Sex;
	}
	public void setSex(char sex) {
		Sex = sex;
	}	
	public int getRating() {
		return Rating;
	}
	public void setRating(int rating) {
		Rating = rating;
	}	
	
}